# Escreva um programa que realize a soma de dois números informados pelo usuário, e retorne o resultado.


print("----- Vamos somar! -----")
numero1 = float(input("Informe um número: "))
numero2 = float(input("Informe outro número: "))
soma = numero1 + numero2

print(f"A soma entre os números {numero1:.0f} e {numero2:.0f} é igual a {soma:.0f}.")